package co.com.coomeva.sipas.core.model.sipasdb;

public class Clitab {
	
	private String codint;
	private String codnom;
	private String codtab;
	private String codpro;
	
	public String getCodint() {
		return codint;
	}
	public void setCodint(String codint) {
		this.codint = codint;
	}
	public String getCodnom() {
		return codnom;
	}
	public void setCodnom(String codnom) {
		this.codnom = codnom;
	}
	public String getCodpro() {
		return codpro;
	}
	public void setCodpro(String codpro) {
		this.codpro = codpro;
	}
	public String getCodtab() {
		return codtab;
	}
	public void setCodtab(String codtab) {
		this.codtab = codtab;
	}
	

}
